  <li>Wikipedia lists of museums by country/county</li>
</ul>
<p>Since then, we have continued to check for new and closed museums and to update the database accordingly. Our new information has come from four main sources</p>
<ul>
<li>Museums Journal news and announcements</li>
<li>Google alerts and online searches</li>
<li>Regular communication with Museums Development Network</li>
<li>Direct contact with museum staff</li>
</ul>
<p>The updated Mapping Museums database provided the basis for our study of closure.</p>
<p>For the Museum Closure project, which ran between 2023-25, we collected additional data on why museums had closed and on what had happened to the collections. Wherever possible we collected information on who had acquired collections, under what circumstances, when, and the objects’ new locations, if relevant. We found our information from the following sources:</p>
<ul>
<li>Local and national newspapers</li>
<li>Subject specialist journals</li>
<li>Charity commission records</li>
<li>Closed museums’ websites (extant and via Wayback machine)</li>
<li>Defunct and current social media</li>
<li>Online reviews, blogs, and chat forums</li>
</ul>
<p>We followed leads by email and telephone, contacting former volunteers, members of staff, and family members as appropriate. We also put out calls for help via social media and contacted staff in:</p>
<ul>
<li>Subject specialist networks</li>
<li>Enthusiast, preservation, and historical societies</li>
<li>Local councils</li>
<li>Museum services</li>
<li>Neighbouring museums</li>
<li>Neighbouring businesses</li>
<li>Tourist offices</li>
</ul>
<p>The material was cross-checked within the research team. Staff from each of the Museums Development Networks reviewed and helped refine our lists of closed museums.</p>
