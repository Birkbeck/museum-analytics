<p>Data Collection has occurred in two main phases. For the original Mapping Museums project, which ran from 2016-2020, we collected information on all the museums open in the UK between 1960 and 2020. We logged their address, governance, size, and subject matter, accreditation status, years of opening and, if relevant, closure. Our information came from the following sources:</p>
<ul>
  <li>Arts Council England list of accredited museums 2017</li>
  <li>Association of Independent Museums members list 2016</li>
  <li>Association of Independent Museums members list 1982</li>
  <li>Association of Independent Museums non-members list 1982</li>
  <li>AMOT Guide to Military Museums in the UK 2010/2011</li>
  <li>Digest of Museum Statistics 1998</li>
  <li>Historic Houses Association Friends Pocket Guide 2016</li>
  <li>Historic Houses & Castles in Great Britain and Northern Ireland, London: Index Publishers 1959</li>
  <li>Kenneth Hudson and Ann Nicholls, The Directory of Museums and Living Displays, London: Macmillan 1985</li>
  <li>Micromuseums Archive, Bishopsgate Institute, London</li>
  <li>Museum and Libraries Division, Wales. Raw data from ‘Spotlight’ reports, 2002, and published data 2007, 2013, 2015</li>
  <li>Museums and Galleries Scotland museums list 2016</li>
  <li>Northern Ireland Museums Council, museum list, 2012, 2016</li>
  <li>Museum Association ‘Find a Museum’ website 2017</li>
